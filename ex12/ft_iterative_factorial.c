int	ft_iterative_factorial(int b)
{
	int i;

	i = b;
	if (b > 1 && b < 13)
		while (--i != 1)
			b = b * i;
	else if ((b < 0) || (b > 12))
		return (0);
	else if ((b == 0) || (b == 1))
		return (1);
	return (b);
}
